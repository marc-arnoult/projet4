<script>
    import Vue from 'vue';
    import VueTranslate from 'vue-translate-plugin';
    import moment from 'moment'
    import _ from 'lodash'
    import store from '../store/ReservationStore'

    Vue.use(VueTranslate);

    export default {
        props: {
            index: {
                type: Number,
                required: false
            }
        },
        data() {
            return {
                store,
                ticket: {
                    first_name:'',
                    last_name: '',
                    birthday: '',
                    country: '',
                    reduction: false,
                    price: 0
                },
            }
        },
        locales: {
            en: {
                'Prix': 'Price',
                'Nom*': 'Last name*',
                'Prenom*': 'First name*',
                'Date de naissance*': 'Birthday*',
                'Nationalité*': 'Nationality*'
            }
        },
        methods: {
            calculatePrice() {
                let now = moment();
                let age = parseInt(now.diff(this.ticket.birthday, 'years'));
                let coef = this.store.type === "Demi-journée" ? 0.5 : 1;

                switch (true) {
                    case age < 4:
                        this.ticket.price = 0;
                        break;

                    case this.ticket.reduction === true:
                        this.ticket.price = 10 * coef;
                        break;

                    case (age >= 4 && age < 12):
                        this.ticket.price = 8 * coef;
                        break;

                    case (age >= 60):
                        this.ticket.price = 12 * coef;
                        break;

                    default:
                        this.ticket.price = 16 * coef;
                        break;
                }
                this.$emit('setPrice')
            }
        },
        watch: {
            ticket: {
                handler: _.debounce(function(ticket) {
                    if (ticket.birthday !== '') {
                        this.calculatePrice()
                    }
                }, 200),
                deep: true
            }
        },
        updated: _.debounce(function () {
            this.$parent.isCompleted()
        }, 200)
    }
</script>

